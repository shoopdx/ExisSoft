    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int rows = tabFactura.getSelectedRow();
        
        int codArt = codigoArt(model.getValueAt(rows, 1).toString()); //necesito
        int cantidad = Integer.parseInt(model.getValueAt(rows, 2).toString());
        String cate = this.catArt(model.getValueAt(rows, 1).toString());//necesito
        int precio = Integer.parseInt(model.getValueAt(rows, 3).toString());
        
        IngresoDAO id = new IngresoDAO();
        int resp = JOptionPane.showConfirmDialog(null, "Desea Actualizar la cantidad " + cantidadAntigua + "(antigua) por " + cantidad+"(nueva) y el precio "+this.precioAntiguo+"(antiguo) por "+precio+"(nuevo)", "Confirmacion", JOptionPane.YES_NO_OPTION);
        
        if (resp == 0) {
            if (id.Editar(idFactura, codArt, cate, cantidad, precio)) {

                StockDAO sd = new StockDAO();
                ArrayList<Stock> stocks = sd.getStock2(cate);
                int newCantidad = 0;

                if (cantidad > cantidadAntigua) {
                    cantidad = cantidad - cantidadAntigua;
                    for (Stock stock : stocks) {
                        int b = stock.getCodigoArt();
                        if (b == codArt) {
                            int a = stock.getStock();
                            newCantidad = a + cantidad;
                        }
                    }

                    sd.actualizar(newCantidad, codArt, cate);

                } else {
                    cantidad = cantidadAntigua - cantidad;
                    for (Stock stock : stocks) {
                        
                        int b = stock.getCodigoArt();
                        if (b == codArt) {
                            int a = stock.getStock();
                            newCantidad = a - cantidad;
                        }
                    }
                    sd.actualizar(newCantidad, codArt, cate);
                }

                JOptionPane.showMessageDialog(null, id.getMessage(), "Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar", "Informacion", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

