    public void cargaTabla (int idFactura, int nroFactura, String fecha){
        
        
        lblNroFactura.setText(String.valueOf(nroFactura));
        lblFecha.setText(fecha);
                
        this.idFactura = idFactura;
        
        IngresoDAO id = new IngresoDAO();
        ArrayList <Ingreso> ingresos = id.getIngresoDetalle(idFactura);
        
        for (Ingreso ingreso : ingresos) {
            Object filas [] = new Object [4];
            filas [0] = ingreso.getCodigoArt();
            filas [1] = articulo(ingreso.getCodigoArt(), ingreso.getCategoria());
            filas [2] = ingreso.getCantidad();
            filas [3] = ingreso.getPrecioUnitario();
            
            model.addRow(filas);
            
            total += ingreso.getPrecioUnitario();
            
        }
        
        tabFactura.setModel(model);
        
        neto = total/1.19;
        iva = (total * 19) / 100;
        
         util u = new util();
        
        lblNeto.setText("$ " + u.formatearNumero(String.valueOf(Math.round(neto))));
        lblIVA.setText("$ " + u.formatearNumero(String.valueOf(Math.round(iva))));
        lblTotal.setText("$ " + u.formatearNumero(String.valueOf(Math.round(total))));
         
        
        
    }
    
    public String articulo (int cod, String cat){
        ArticuloDAO ad = new ArticuloDAO();
        ArrayList <Articulo> articulos = ad.getAxC(cat);
        
        String nombre = "";
        
        for (Articulo articulo : articulos) {
            if(articulo.getCodigoArt() == cod ){
                nombre = articulo.getNombreArt();
                break;
            }
        }
        return nombre;
    }
    public int codigoArt(String nombre) {
        ArticuloDAO ad = new ArticuloDAO();
        ArrayList<Articulo> articulos = ad.getArticulos();
        int cod = 0;
        for (Articulo articulo : articulos) {
            if (articulo.getNombreArt().equals(nombre)) {
                cod = articulo.getCodigoArt();
                break;
            }
        }
        return cod;
    }
    
    public String catArt(String nombre) {
        ArticuloDAO ad = new ArticuloDAO();
        ArrayList<Articulo> articulos = ad.getArticulos();
        String cod = "";
        for (Articulo articulo : articulos) {
            if (articulo.getNombreArt().equals(nombre)) {
                cod = articulo.getCategoria();
                break;
            }
        }
        return cod;
    }

