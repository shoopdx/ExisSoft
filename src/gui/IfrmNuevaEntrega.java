    private void cargaCbo() {
        DepartamentoDAO dd = new DepartamentoDAO();
        ArrayList<Departamento> departamentos = dd.getDepartamentos();

        for (Departamento departamento : departamentos) {
            cboDepartamento.addItem(departamento.getNombreDepa());
        }
    }

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        ArticuloDAO ad = new ArticuloDAO(); //traer articulos recorrerlos e igualarlo con el codigo 
        ArrayList<Articulo> articulos = ad.getArticulos();

        Object filas[] = new Object[6];
        int precio = Integer.parseInt(txtPrecio.getText());
        if (chkIva.isSelected()) {
            precio = precio + ((precio * 19) / 100);
        }

        filas[0] = buscaCodigo(cboArticulos.getSelectedItem().toString());
        filas[1] = cboArticulos.getSelectedItem();
        filas[2] = cboCat.getSelectedItem();
        filas[3] = txtCantidad.getText();
        filas[4] = precio;
        filas[5] = cboDepartamento.getSelectedItem().toString();

        model.addRow(filas);
        tabArticulos.setModel(model);


    }//GEN-LAST:event_btnAgregarActionPerformed

