    private void btnAgregarEntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEntregaActionPerformed

        int rows1 = model.getRowCount();

        String dia = String.valueOf(dteFechaI.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = String.valueOf(dteFechaI.getCalendar().get(Calendar.MONTH) + 1);
        String anio = String.valueOf(dteFechaI.getCalendar().get(Calendar.YEAR));
        String fecha = anio + "-" + mes + "-" + dia;
        
        int aviso = 0;

        for (int i = 0; i < rows1; i++) {

            int stockA = validar(i);

            if (stockA >= 0) {

                EntregaDAO ed = new EntregaDAO();

                int rows = model.getRowCount();

                    int cantidad = Integer.parseInt(model.getValueAt(i, 3).toString());
                    int codigoArt = Integer.parseInt(model.getValueAt(i, 0).toString());
                    int precio = Integer.parseInt(model.getValueAt(i, 4).toString());
                    String cat = model.getValueAt(i, 2).toString();
                    String depa = model.getValueAt(i,5).toString();
                    if (ed.agregar(codigoArt, depa, fecha, cantidad, precio, cat)) {
                        StockDAO sd = new StockDAO();

                        ArrayList<Stock> stocks = sd.getStock2(model.getValueAt(i, 2).toString());

                        int newCantidad = 0;
                        for (Stock stock : stocks) {
                            int b = stock.getCodigoArt();
                            if (b == codigoArt && stock.getCategoria().equals(cat)) {
                                int a = stock.getStock();
                                newCantidad = a - cantidad;
                                
                                sd.actualizar(newCantidad, codigoArt, cat);
                                
                                break;
                            }
                        }
                    


                    } else {
                        String nombre = tabArticulos.getValueAt(i, 1).toString();
                        JOptionPane.showMessageDialog(null, "Producto  " + nombre + ", No fue entregado\n"+ed.getMessage());
                        aviso = 1;
                    }
                

                
            } else {
                aviso = 1;
                String nombre = tabArticulos.getValueAt(i, 1).toString();
                JOptionPane.showMessageDialog(null, "El articulo '" + nombre + "', supera el stock", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        if (aviso == 0) {
            JOptionPane.showMessageDialog(null, "Tarea realizada", "Informacion", JOptionPane.INFORMATION_MESSAGE);
        }
        
        int rowsTotal = model.getRowCount();
        
        for (int i = 0; i <= rowsTotal; i++) {
            model.removeRow(0);
        }

        cboDepartamento.setSelectedIndex(0);

        txtCantidad.setText("");
        txtPrecio.setText("");


    }//GEN-LAST:event_btnAgregarEntregaActionPerformed

    private void cboCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCatActionPerformed
        cboArticulos.removeAllItems();

        ArticuloDAO ad = new ArticuloDAO();
        ArrayList<Articulo> articulos = ad.getAparaCombo(cboCat.getSelectedItem().toString());

        for (Articulo articulo : articulos) {
            cboArticulos.addItem(articulo.getNombreArt());
        }
    }//GEN-LAST:event_cboCatActionPerformed

    public int validar(int i) {
        StockDAO sd = new StockDAO();

        int rows = model.getRowCount();
        int newCantidad = 0;
        for (i = i; i < rows; i++) {
            ArrayList<Stock> stocks = sd.getStock2(model.getValueAt(i, 2).toString());
            int cantidad = Integer.parseInt(model.getValueAt(i, 3).toString());
            int codigoArt = Integer.parseInt(model.getValueAt(i, 0).toString());
            String cate = model.getValueAt(i, 2).toString();

            for (Stock stock : stocks) {
                int b = stock.getCodigoArt();
                if (b == codigoArt && stock.getCategoria().equals(cate)) {
                    int a = stock.getStock();
                    newCantidad = a - cantidad;
                    break;
                }
            }

        }
        return newCantidad;
    }

