    private void cboCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCatActionPerformed
        ArticuloDAO ad = new ArticuloDAO();
        ArrayList<Articulo> articulos = ad.getAxC(cboCat.getSelectedItem().toString());

        int totalfilas = model.getRowCount();

        for (int i = 0; i < totalfilas; i++) {
            model.removeRow(0);
        }

        for (Articulo articulo : articulos) {
            Object filas[] = new Object[5];
            filas[0] = articulo.getCodigoArt();
            filas[1] = articulo.getNombreArt();
            filas[2] = buscarStock(articulo.getCodigoArt(), cboCat.getSelectedItem().toString());
            filas[3] = saldo(articulo.getCodigoArt(), cboCat.getSelectedItem().toString());
            filas[4] = saldo(articulo.getCodigoArt(), cboCat.getSelectedItem().toString()) * buscarStock(articulo.getCodigoArt(), cboCat.getSelectedItem().toString());

            model.addRow(filas);

        }
        tabArticulos.setModel(model);
    }//GEN-LAST:event_cboCatActionPerformed

