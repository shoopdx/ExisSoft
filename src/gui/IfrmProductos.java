    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        List productos = new ArrayList();
        int totalArt = tabArticulos.getRowCount();
        int saldototal = 0;

        for (int i = 0; i < totalArt; i++) {
            ReporteProductos lista = new ReporteProductos();
            lista.setFolio(tabArticulos.getValueAt(i, 0).toString());
            lista.setNombre(tabArticulos.getValueAt(i, 1).toString());
            lista.setStock(tabArticulos.getValueAt(i, 2).toString());
            lista.setPrecio(tabArticulos.getValueAt(i, 3).toString());
            lista.setSaldo(tabArticulos.getValueAt(i, 4).toString());
            saldototal = saldototal + Integer.parseInt(tabArticulos.getValueAt(i, 4).toString());
            productos.add(lista);

        }

        String ruta = System.getProperty("user.dir") + "\\reportes\\ReporteProductos.jasper";

        Map parametro = new HashMap();
        int total = tabArticulos.getRowCount();
        parametro.put("nOfFiscalizador", txtnOfFiscalizador.getText());
        parametro.put("gOfFiscalizador", txtgOfFiscalizador.getText());
        parametro.put("nOfFinanzas", txtnOfFinanzas.getText());
        parametro.put("gOfFinanzas", txtgOfFinanzas.getText());
        parametro.put("nJAdmin", txtnJefeAdm.getText());
        parametro.put("gJAdmin", txtgJefeAdm.getText());

        NumberFormat formatea = NumberFormat.getNumberInstance();
        int valor = saldototal;
        formatea.setMaximumFractionDigits(0);
        String nuevototal = formatea.format(valor);

        parametro.put("total", nuevototal);

        if (cboCat.getSelectedItem().toString().equals("2204001")) {
            parametro.put("titulo", "Materiales de Oficina");
        }
        if (cboCat.getSelectedItem().toString().equals("2204007")) {
            parametro.put("titulo", "Materiales y Utiles de Aseo");
        }
        if (cboCat.getSelectedItem().toString().equals("2204009")) {
            parametro.put("titulo", "Insumos Computacionales");
        }

        Imprimir imp = new Imprimir();
        imp.Impresion(ruta, parametro, productos);
    }//GEN-LAST:event_btnImprimirActionPerformed

