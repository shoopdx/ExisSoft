    public int saldo(int cod, String cat) {
        IngresoDAO ed = new IngresoDAO();
        ArrayList<Ingreso> ingresos = ed.getIngresoxC(cat);
        ArticuloDAO ad = new ArticuloDAO();
        ArrayList<Articulo> articulos = ad.getAxC(cat);
        int precio = 0;
        int i = 0;
        for (Ingreso ingreso : ingresos) {

            if (ingreso.getCodigoArt() == cod) {
                precio = precio + ingreso.getPrecioUnitario();
                i++;
            }
        }

        if (precio != 0) {
            precio = (precio / i);
            return precio;
        } else {
            return 0;
        }
    }

    public int buscarStock(int cod, String cat) {
        StockDAO sd = new StockDAO();
        ArrayList<Stock> stocks = sd.getStock2(cat);
        int total = 0;
        for (Stock stock : stocks) {
            if (cod == stock.getCodigoArt()) {

                total = stock.getStock();

            }
        }
        return total;
    }

